# Enumerate required environment variables for each resource type.
requiredEnv() {
  local key="${1/-/_}"
  local lookup="$key[*]"
  local pki=(
    KWM_CLUSTER_NAME
    KWM_APISERVER_PUBLIC_IP
    KWM_LOCAL_PKI_PATH
    KWM_ETCD_CLIENT_SANS
  )
  local cluster_admin=(
    KWM_CLUSTER_NAME
    KWM_LOCAL_PKI_PATH
    KWM_APISERVER_PUBLIC_IP
  )
  local cni_manifest=(KWM_VERSION_KUBE_ROUTER)
  local controlplane_node=(
    KWM_CONNECT
    KWM_PRIVATE_IP
    KWM_HOSTNAME
    KWM_ROLE
    KWM_APISERVER_PRIVATE_IP
    KWM_CLUSTER_NAME
    KWM_VERSION_KUBERNETES
    KWM_VERSION_CNI_PLUGIN
    KWM_VERSION_CRI_CONTAINERD
    KWM_POD_CIDR
    KWM_SERVICE_CIDR
    KWM_KUBERNETES_SERVICE_IP
    KWM_DNS_SERVICE_IP
    KWM_APISERVER_PUBLIC_IP
    KWM_ETCD_SERVERS
    KWM_LOCAL_PKI_PATH
    KWM_CONFIG_PATH
  )
  local dns_manifest=(KWM_VERSION_KUBE_DNS KWM_DNS_SERVICE_IP)
  local etcd_node=(
    KWM_CONNECT
    KWM_PRIVATE_IP
    KWM_HOSTNAME
    KWM_ETCD_INITIAL_CLUSTER
    KWM_VERSION_ETCD
    KWM_LOCAL_PKI_PATH
    KWM_CONFIG_PATH
  )
  local startup=(
    KWM_CLUSTER_NAME
    KWM_APISERVER_PUBLIC_IP
    KWM_APISERVER_PRIVATE_IP
    KWM_POD_CIDR
    KWM_SERVICE_CIDR
    KWM_KUBERNETES_SERVICE_IP
    KWM_DNS_SERVICE_IP
    KWM_CONFIG_PATH
    KWM_LOCAL_PKI_PATH
    KWM_VERSION_ETCD
    KWM_VERSION_KUBERNETES
    KWM_VERSION_KUBE_DNS
    KWM_VERSION_CNI_PLUGIN
    KWM_VERSION_CRI_CONTAINERD
    KWM_VERSION_KUBE_ROUTER
  )
  local worker_node=(
    KWM_CONNECT
    KWM_PRIVATE_IP
    KWM_HOSTNAME
    KWM_ROLE
    KWM_APISERVER_PRIVATE_IP
    KWM_CLUSTER_NAME
    KWM_VERSION_KUBERNETES
    KWM_VERSION_CNI_PLUGIN
    KWM_VERSION_CRI_CONTAINERD
    KWM_POD_CIDR
    KWM_LOCAL_PKI_PATH
    KWM_CONFIG_PATH
  )
  [[ -z "${!lookup}" ]] && exit 1
  printf "${!lookup}"
}
